    public void fillTable(List<ClothingResponseDTO> list, ClothingPiece p) throws Exception {
        DefaultTableModel model = new DefaultTableModel();

        fillTableAux(model, p);
        if (list == null) {
            return;
        }

        System.out.println("DENTRO DE FILL TABLE");
        if (!list.isEmpty()) {
            for (ClothingResponseDTO data : list) {
                ShirtResponseDTO shirtData = (ShirtResponseDTO) data;
                model.addRow(new Object[]{
                    shirtData.id(),
                    shirtData.color(),
                    shirtData.price(),
                    shirtData.quantity(),
                    shirtData.brand(),
                    shirtData.size(),
                    shirtData.gender(),
                    shirtData.closureType(),
                    shirtData.clothingType(),
                    shirtData.pattern(),
                    shirtData.fabric(),
                    shirtData.pocket(),
                    shirtData.collar(),
                    shirtData.sleeve()
                });
            }
        }
        System.out.println("DENO FINAL DE FILL TABLE");
        jTable2.setModel(model);
    }

