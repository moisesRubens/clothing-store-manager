    private void updateTable(Boolean allClothings, ClothingPiece p) throws Exception {
        List<ClothingResponseDTO> listSearchToRemove = new ArrayList<>();
        try {
            if (allClothings == true) {
                listSearchToRemove = clothingController.getAllClothing();
            } else {
                switch (p) {
                    case ClothingPiece.SHIRT -> {
                        ShirtRequestDTO shirtData = searchShirtToRemove();
                        listSearchToRemove = clothingController.consultClothings(shirtData);
                    }
                    case ClothingPiece.PANT -> {
                        PantRequestDTO pantieData = consultarCalca();
                        listSearchToRemove = clothingController.consult(pantieData);
                    }
                }
            }
        } catch (Exception e) {

        } finally {
            fillTable(listSearchToRemove, p);
        }
    }

    private ShirtRequestDTO searchShirtToRemove() throws Exception {
        Integer collar = -1;
        Integer sleeve = -1;
        Integer quantity = -1;
        Integer pocket = (jTextFieldRemovePocket.getText().isEmpty()) ? -1 : Integer.valueOf(jTextFieldRemovePocket.getText());
        Double price = (jTextFieldRemovePrice.getText().isEmpty()) ? -1D : Double.valueOf(jTextFieldRemovePrice.getText());
        String pattern = jTextFieldRemovePattern.getText();
        String style = jTextFieldRemoveStyle.getText();
        String brand = jTextFieldRemoveBrand.getText();
        String fabric = jTextFieldRemoveFabric.getText();
        String color = jTextFieldRemoveColor.getText();
        String closureType = jTextFieldRemoveClosureType.getText();
        ShirtSize shirtSize = null;
        Gender gender = null;

        if (jTextFieldRemoveSize.getText().equals("P")) {
            shirtSize = ShirtSize.SMALL;
        } else if (jTextFieldRemoveSize.getText().equals("M")) {
            shirtSize = ShirtSize.MEDIUM;
        } else if (jTextFieldRemoveSize.getText().equals("G")) {
            shirtSize = ShirtSize.LARGE;
        } else if (!jTextFieldRemoveSize.getText().isEmpty() || !jTextFieldRemoveSize.getText().isBlank()) {
            throw new IllegalArgumentException("INSIRA UM TAMANHO VALIDO");
        }

        if (jTextFieldRemoveGender.getText().equals("M")) {
            gender = Gender.MALE;
        } else if (jTextFieldRemoveGender.getText().equals("F")) {
            gender = Gender.FEMALE;
        } else if (!jTextFieldRemoveGender.getText().isEmpty() || !jTextFieldRemoveGender.getText().isBlank()) {
            throw new IllegalArgumentException("INSIRA UM GENERO VALIDO");
        }

        if ((jCheckBoxRemoveCollarSim.isSelected() && jCheckBoxRemoveCollarNao.isSelected())
                || (jCheckBoxRemoveSleeveSim.isSelected() && jCheckBoxRemoveSleeveNao.isSelected())) {
            throw new IllegalArgumentException("INSIRA APENAS UM VALOR PARA GOLA E MANGA");
        }

        if (jCheckBoxRemoveCollarSim.isSelected()) {
            collar = 1;
        } else if (jCheckBoxRemoveCollarNao.isSelected()) {
            collar = 0;
        }

        if (jCheckBoxRemoveSleeveSim.isSelected()) {
            sleeve = 1;
        } else if (jCheckBoxRemoveSleeveNao.isSelected()) {
            sleeve = 0;
        }

        return new ShirtRequestDTO(color, price, quantity, fabric, brand, style,
                gender, pattern, pocket, closureType, ClothingType.STANDARD,
                sleeve, collar, shirtSize);
    }

    private PantRequestDTO consultarCalca() throws Exception {
        Integer size = -1;
        Integer pocket = (jTextFieldConsultCalcaPocket.getText().isBlank()) ? -1 : Integer.valueOf(jTextFieldConsultCalcaPocket.getText());
        Integer quantity = (jTextFieldRemoverCalcaQuantity.getText().isBlank()) ? -1 : Integer.valueOf(jTextFieldRemoverCalcaQuantity.getText());
        Double price = (jTextFieldConsultCalcaPrice.getText().isBlank()) ? -1D : Double.valueOf(jTextFieldConsultCalcaPrice.getText());
        String pattern = jTextFieldConsultCalcaPattern.getText();
        String style = jTextFieldConsultCalcaStyle.getText();
        String brand = jTextFieldConsultCalcaBrand.getText();
        String fabric = jTextFieldConsultCalcaFabric.getText();
        String color = jTextFieldConsultCalcaColor.getText();
        String closureType = jTextFieldConsultCalcaClosureType.getText();
        HemType hemType = null;
        WaistType waistType = null;
        PantLengthType length = null;
        Gender gender = null;

        if ((jCheckBoxCalcaComprimentoCurto.isSelected() && jCheckBoxCalcaComprimentoNormal.isSelected())
                || (jCheckBoxCalcaComprimentoCurto.isSelected() && jCheckBoxCalcaComprimentoLongo.isSelected())
                || (jCheckBoxCalcaComprimentoNormal.isSelected() && jCheckBoxCalcaComprimentoLongo.isSelected())) {
            throw new IllegalArgumentException("Selecione apenas uma opcao de comprimento");
        }
        if ((jCheckBoxConsultCalcaTipoBarraDobrada.isSelected() && jCheckBoxConsultCalcaTipoBarraReta.isSelected())
                || (jCheckBoxConsultCalcaTipoBarraDobrada.isSelected() && jCheckBoxConsultCalcaTipoBarraElastica.isSelected())
                || (jCheckBoxConsultCalcaTipoBarraElastica.isSelected() && jCheckBoxConsultCalcaTipoBarraReta.isSelected())) {
            throw new IllegalArgumentException("Selecione apenas uma opcao de tipo de barra");
        }
        if ((jCheckBoxTipoCinturaBaixa.isSelected() && jCheckBoxTipoCinturaMedia.isSelected())
                || (jCheckBoxTipoCinturaBaixa.isSelected() && jCheckBoxTipoCinturaAlta.isSelected())
                || (jCheckBoxTipoCinturaBaixa.isSelected() && jCheckBoxTipoCinturaElastica.isSelected())
                || (jCheckBoxTipoCinturaMedia.isSelected() && jCheckBoxTipoCinturaAlta.isSelected())
                || (jCheckBoxTipoCinturaMedia.isSelected() && jCheckBoxTipoCinturaElastica.isSelected())
                || (jCheckBoxTipoCinturaAlta.isSelected() && jCheckBoxTipoCinturaElastica.isSelected())) {
            throw new IllegalArgumentException("Selecione apenas uma opcao de cintura");
        }

        if (jCheckBoxConsultCalcaTipoBarraReta.isSelected()) {
            hemType = HemType.STRAIGHT;
        } else if (jCheckBoxConsultCalcaTipoBarraDobrada.isSelected()) {
            hemType = HemType.CUFFED;
        } else if (jCheckBoxConsultCalcaTipoBarraElastica.isSelected()) {
            hemType = HemType.ELASTIC;
        }

        if (jCheckBoxConsultCalcaTipoCinturaBaixa.isSelected()) {
            waistType = WaistType.LOW;
        } else if (jCheckBoxConsultCalcaTipoCinturaMedia.isSelected()) {
            waistType = WaistType.MID;
        } else if (jCheckBoxConsultCalcaTipoCinturaAlta.isSelected()) {
            waistType = WaistType.HIGH;
        } else if (jCheckBoxConsultCalcaTipoCinturaElastica.isSelected()) {
            waistType = WaistType.ELASTIC;
        }

        if (jCheckBoxConsultCalcaComprimentoCurto.isSelected()) {
            length = PantLengthType.SHORT;
        } else if (jCheckBoxConsultCalcaComprimentoNormal.isSelected()) {
            length = PantLengthType.REGULAR;
        } else if (jCheckBoxConsultCalcaComprimentoLongo.isSelected()) {
            length = PantLengthType.LONG;
        }

        if (jTextFieldConsultGender.getText().equals("M")) {
            gender = Gender.MALE;
        } else if (jTextFieldConsultGender.getText().equals("F")) {
            gender = Gender.FEMALE;
        } else if (!jTextFieldConsultGender.getText().isBlank()) {
            throw new IllegalArgumentException("INSIRA UM GENERO VALIDO");
        }

        PantRequestDTO pantieData = new PantRequestDTO(color, quantity, fabric, brand, style,
                gender, pattern, pocket, closureType, ClothingType.STANDARD,
                hemType, waistType, length, size, price);

        return pantieData;
    }

